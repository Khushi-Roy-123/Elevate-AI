    'function init(){',
    '  pts=[];',
    '  for(var i=0;i<60;i++)pts.push([rnd(50,200),rnd(50,270)]);',
    '  for(var i=0;i<40;i++)pts.push([rnd(250,400),rnd(150,270)]);',
