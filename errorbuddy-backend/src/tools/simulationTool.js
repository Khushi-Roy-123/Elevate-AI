'use strict';

function generate_visual_simulation({ type, code_snippet, error_description }) {
  const desc = (error_description || '').replace(/'/g, '&#39;');
  const templates = {
    gradient_descent: gradientDescentHTML(desc),
    linear_regression: gradientDescentHTML(desc),
    bst_traversal: bstTraversalHTML(desc),
    merge_sort: mergeSortHTML(desc),
    bubble_sort: bubbleSortHTML(desc),
    neural_net_forward_pass: neuralNetHTML(desc),
    k_means_clustering: kMeansHTML(desc),
  };
  return templates[type] || defaultSimHTML(type, desc);
}

